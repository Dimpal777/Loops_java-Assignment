public class EqualNotequal {
    public static void main(String[] args) {
        int num1 = 40;
        int num2 = 20;

        // Using the equal operator (==)
        if (num1 == num2) {
            System.out.println("num1 is equal to num2");
        } else {
            System.out.println("num1 is not equal to num2");
        }

        // Using the not equal operator (!=)
        if (num1 != num2) {
            System.out.println("num1 is not equal to num2");
        } else {
            System.out.println("num1 is equal to num2");
        }
    }
}